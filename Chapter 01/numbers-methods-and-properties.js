let num = 42;

console.log(num.toString()); // "42", a string representation of the number
console.log(num.toFixed(2)); // "42.00", the number with 2 digits after the decimal point
console.log(num.toExponential(2)); // "4.20e+1", the number in exponential notation

console.log(Number.MAX_VALUE); // 1.7976931348623157e+308
console.log(Number.isInteger(num)); // true
console.log(Number.parseInt("42")); // 42
